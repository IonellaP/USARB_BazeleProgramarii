package Structura_Liniara;

public class Ex9_PerimetrulPatratului {
    public static void main(String[] args) {
        double S = 45.5;
        double Latura = Math.sqrt(S);
        double Perimetru = 4 * Latura;

        System.out.println("Latura este: " + Latura);
        System.out.println("Perimetrul este: " + Perimetru);
    }
}
